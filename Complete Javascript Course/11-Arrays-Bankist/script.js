const dispayMovements = function (movements) {
  containerMovements.innerHTML = '';
  movements.forEach(function (mov, i) {
    const type = mov > 0 ? 'deposit' : 'withdrawal';
    const hmtl = `
    <div class="movements__row">
          <div class="movements__type movements__type--${type}">${
      i + 1
    } ${type}</div>
          
          <div class="movements__value">${mov}</div>
        </div>`;

    containerMovements.insertAdjacentHTML('afterbegin', hmtl);
  });
};

// Create username
function createUserName(nameOfUsers) {
  nameOfUsers.forEach(function (acc) {
    acc.username = acc.owner
      .toLowerCase()
      .split(' ')
      .map(name => name[0])
      .join('');
  });
}

createUserName(accounts);

function updateUI(loggedAccount) {
  // Display movements
  dispayMovements(loggedAccount.movements);
  // Display balance
  callDisplayBalance(loggedAccount);
  // Display summary
  calcDisplaySummary(loggedAccount);
}

// Display Balance
const callDisplayBalance = function (loggedAccount) {
  loggedAccount.balance = loggedAccount.movements.reduce(function (
    acc,
    cur,
    i,
    array
  ) {
    return acc + cur;
  },
  0);
  labelBalance.textContent = `${loggedAccount.balance}€`;
};

// Display summary
function calcDisplaySummary(loggedAccount) {
  const incomes = loggedAccount.movements
    .filter(mov => mov > 0)
    .reduce((acc, mov) => acc + mov);
  labelSumIn.textContent = `${incomes}€`;

  const expense = loggedAccount.movements
    .filter(mov => mov < 0)
    .reduce((acc, mov) => acc + mov);
  labelSumOut.textContent = `${Math.abs(expense)}€`;

  const interest = loggedAccount.movements
    .filter(mov => mov > 0)
    .map(deposit => (deposit * loggedAccount.interestRate) / 100)
    .filter(interests => interests > 1)
    .reduce((acc, mov) => acc + mov);
  labelSumInterest.textContent = `${interest}€`;
}

// Event handlers
let currentAccount;

btnLogin.addEventListener('click', function (e) {
  // Prevent from submitting
  e.preventDefault();

  currentAccount = accounts.find(
    acc => acc.username === inputLoginUsername.value
  );
  console.log(currentAccount);

  if (currentAccount?.pin === Number(inputLoginPin.value)) {
    // Display UI and message
    labelWelcome.textContent = `Welcome back, ${
      currentAccount.owner.split(' ')[0]
    }`;
    containerApp.style.opacity = 100;

    // Clear input fields
    inputLoginUsername.value = inputLoginPin.value = '';
    inputLoginUsername.blur();
    inputLoginPin.blur();

    updateUI(currentAccount);
  }
});

btnTransfer.addEventListener('click', function (e) {
  e.preventDefault();
  const amount = Number(inputTransferAmount.value);
  const receiverAcc = accounts.find(
    acc => acc.username === inputTransferTo.value
  );

  inputTransferAmount.value = inputTransferTo.value = '';

  if (
    receiverAcc &&
    amount > 0 &&
    currentAccount.balance >= amount &&
    receiverAcc?.username !== currentAccount.username
  ) {
    currentAccount.movements.push(-amount);
    receiverAcc.movements.push(amount);

    updateUI(currentAccount);
  }
});

btnClose.addEventListener('click', function (e) {
  e.preventDefault();
  if (
    inputCloseUsername.value === currentAccount.username &&
    Number(inputClosePin.value) === currentAccount.pin
  ) {
    const index = accounts.findIndex(
      acc => acc.username === currentAccount.username
    );

    // Hide UI
    containerApp.style.opacity = 0;
    // Delete account
    accounts.splice(index, 1);
  }
  inputCloseUsername.value = inputClosePin.value = '';
});

